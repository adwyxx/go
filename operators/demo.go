package main

import "fmt"

func main(){

	fmt.Println("23.121/3 = ",23.121/3) //23.121/3= 7.707
	fmt.Println("23.121/3.1234 = ",23.121/3.1234) //23.121/3.1234 = 7.402510085163604

	fmt.Println("23/3.12 = ",23/3.12 ) //23/3.12 =  7.371794871794871

	fmt.Println("2&5=",2&5) //2&5= 0
	fmt.Println("2|5=",2|5)	//2|5= 7

	fmt.Println("-3&2=",-3&2) //2&-3= 0
	fmt.Println("-3|2=",-3|2) //2|-3= -1

	fmt.Println("-3^2=",-3^2) //2|-3= -1

	fmt.Println("-3>>2=",-3>>2) //2|-3= -1
	fmt.Println("-3<<2=",-3<<2) //2|-3= -1
}

